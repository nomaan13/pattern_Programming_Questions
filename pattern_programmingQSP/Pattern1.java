package pattern_programmingQSP;

public class Pattern1 {
    public static void main(String[] args) {

        for (int i =1;i<=5;i++){
            System.out.println(i);
        }
        System.out.println("===================================================================================");
        for (int j =1;j<=5;j++){
            System.out.print(j+" ");
        }
        System.out.println("\n===================================================================================");
        for (int k =5;k>=0;k--){
            System.out.print(k+" ");
        }
        System.out.println("\n===================================================================================");
    }
}
